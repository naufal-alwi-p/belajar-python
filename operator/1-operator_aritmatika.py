# Operator aritmatika adalah operator yang biasa kita temukan untuk operasi matematika.

x = 5
y = 7

# Unary Operator
# Positif (+)
print(+8)

# Negatif (-)
print(-3)

print()

# Binary Operator
# Penjumlahan
print("x + y =", x + y)

# Pengurangan
print("x - y =", x - y)

# Perkalian
print("x * y =", x * y)

# Pembagian (Dalam pyton, nilai hasil pembagian selalu float)
print("x / y =", x / y)

# Pembagian dibulatkan kebawah (Kalau yang ini mengembalikan interger)
print("x // y =", x // y)

# Modulus (Sisa Bagi)
print("x % y =", x % y)

# Perpangkatan
print("x ** y =", x ** y)

print()

# Seperti aritmatika pada umumnya, terdapat urutan prioritas operasi
print("x + 5 / 5 + y =", x + 5 / 5 + y)
print("(x + 5) / 5 + y =", (x + 5) / 5 + y)